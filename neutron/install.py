def get_platform():
    system = platform.system().lower()
    if system == "windows":
        return "windows"
    elif system == "darwin":
        return "macos"
    else:
        return "linux"


def check_adb():
    try:
        result = subprocess.run(
            ["adb", "version"], capture_output=True, text=True, timeout=5
        )
        return "Android Debug Bridge" in result.stdout
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return False


def check_device():
    try:
        result = subprocess.run(
            ["adb", "devices"], capture_output=True, text=True, timeout=5
        )
        lines = result.stdout.strip().split("\n")
        devices = [l for l in lines[1:] if l.strip() and "device" in l]
        return len(devices) > 0
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return False


def adb_shell(cmd):
    try:
        result = subprocess.run(
            ["adb", "shell", cmd], capture_output=True, text=True, timeout=30
        )
        return result.stdout.strip()
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return ""


def backup_vendor():
    print("[*] Creating backup...")
    BACKUP_DIR.mkdir(parents=True, exist_ok=True)
    backup_path = BACKUP_DIR / "vendor_backup"
    
    print(f"    Backing up vendor partition...")
    subprocess.run(["adb", "shell", "su -c 'dd if=/dev/block/by-name/vendor of=/sdcard/vendor_backup.img'"], timeout=300)
    subprocess.run(["adb", "pull", "/sdcard/vendor_backup.img", str(backup_path)], timeout=300)
    subprocess.run(["adb", "shell", "rm /sdcard/vendor_backup.img"], timeout=30)
    
    print(f"[+] Backup saved to: {backup_path}")
    return backup_path


def generate_debloat_bash():
    packages = TWEAKS["debloat"]["packages_to_remove"]
    services = TWEAKS["debloat"]["services_to_disable"]
    
    lines = ['# Remove bloatware packages', 'echo "[*] Removing bloatware..."']
    for pkg in packages:
        lines.append(f'$ADB shell "$SU pm uninstall -k --user 0 {pkg}" 2>/dev/null && echo "    Removed: {pkg}" || echo "    Skipped: {pkg}"')
    
    lines.append('')
    lines.append('# Disable telemetry services')
    lines.append('echo "[*] Disabling telemetry..."')
    for svc in services:
        lines.append(f'$ADB shell "$SU pm disable {svc}" 2>/dev/null && echo "    Disabled: {svc}" || echo "    Skipped: {svc}"')
    
    lines.append('echo "[+] Debloat complete!"')
    return '\n'.join(lines)


