def generate_prop_bash(tweak_name):
    props = TWEAKS[tweak_name]["build_prop"]
    lines = [f'# Apply {tweak_name} tweaks', f'echo "[*] Applying {tweak_name} tweaks..."']
    
    for key, value in props.items():
        lines.append(f'$ADB shell "$SU sed -i /^{key}/d /vendor/build.prop"')
        lines.append(f'$ADB shell "$SU echo \\"{key}={value}\\" >> /vendor/build.prop"')
        lines.append(f'echo "    Set: {key}={value}"')
    
    lines.append(f'echo "[+] {tweak_name.capitalize()} tweaks applied!"')
    return '\n'.join(lines)


